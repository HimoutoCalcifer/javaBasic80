package javaBasic60;

public class proj13_rectangleAreaandPerimeter {

	public static void main(String[] args) {
		
		double w = 5.6;
		double l = 8.5;
		double p = 2.0*(w + l);
		double a = w * l;
		
		System.out.println("The Area of " + w + " x " + l +" = " + a);
		System.out.println("The Perimeter of " + "2("+ w + " + " + l +") = "+ p);

	}

}
